// Regenerate src/leaflet-css.ts from the installed leaflet stylesheet.
// Leaflet renders inside the card shadow root, so its CSS has to ship as a
// string constant instead of a <link>. url() references are dropped: the only
// ones upstream are the layers control icons, which this card never renders.
import { readFileSync, writeFileSync } from "node:fs";

const src = readFileSync("node_modules/leaflet/dist/leaflet.css", "utf8");
const cleaned = src
  .replace(/url\([^)]*\)/g, "none")
  .replace(/`/g, "\\`")
  .replace(/\$\{/g, "\\${");

writeFileSync(
  "src/leaflet-css.ts",
  `// GENERATED by scripts/gen-leaflet-css.mjs, do not edit by hand.\n` +
    `// Source: node_modules/leaflet/dist/leaflet.css\n` +
    `export const LEAFLET_CSS = \`\n${cleaned}\`;\n`
);
console.log("wrote src/leaflet-css.ts", cleaned.length, "chars");
